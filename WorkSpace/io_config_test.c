//设置PortC的第12个IO(PC11)为推挽输出,速度为100MHz, 
//不带上下拉, 并输出高电平. 则寄存器配置代码如下:
void io_config_test(void)
{
	RCC->AHB1ENR |= (1<<2);		//enable GPIOC
	GPIOC->MODER &= ~(3<<(11*2));	//clear the Mode Register
	GPIOC->MODER |= (1<<(11*2));	//设为通用输出功能
	GPIOC->OTYPER &= ~(1<<11);		//设为推挽输出
	GPIOC->OSPEEDR |= (3<<(11*2));	//速度设为100MHz
	GPIOC->PUPDR &= ~(3<<(11*2));	//无上下拉
	GPIOC->ODR |= (1<<11);		//输出高电平
}





